    <h3 class="text-primary">
      Hypersensitivity Reactions
    </h3>

    <p>
      SKYRIZI® (risankizumab-rzaa) is contraindicated in patients with a
      history of serious hypersensitivity reaction to risankizumab-rzaa or any
      of the excipients. Serious hypersensitivity reactions, including
      anaphylaxis, have been reported with the use of SKYRIZI. If a serious
      hypersensitivity reaction occurs, discontinue SKYRIZI and initiate
      appropriate therapy immediately.
    </p>

    <h3 class="text-primary">
      Infection
    </h3>

    <p>
      SKYRIZI may increase the risk of infection. Do not initiate treatment with
      SKYRIZI in patients with a clinically important active infection until it
      resolves or is adequately treated.
    </p>

    <p>
      In patients with a chronic infection or a history of recurrent infection,
      consider the risks and benefits prior to prescribing SKYRIZI. Instruct
      patients to seek medical advice if signs or symptoms of clinically
      important infection occur. If a patient develops such an infection or is
      not responding to standard therapy, closely monitor and discontinue
      SKYRIZI until the infection resolves.
    </p>

    <h3 class="text-primary">
      Tuberculosis (TB)
    </h3>

    <p>
      Prior to initiating treatment with SKYRIZI, evaluate for TB infection and
      consider treatment in patients with latent or active TB for whom an
      adequate course of treatment cannot be confirmed. Monitor patients for
      signs and symptoms of active TB during and after SKYRIZI treatment. Do not
      administer SKYRIZI to patients with active TB.
    </p>

    <h3 class="text-primary">
      Hepatotoxicity in Treatment of Inflammatory Bowel Disease
    </h3>

    <p>
      Drug-induced liver injury was reported in a patient with Crohn’s disease
      who was hospitalized for a rash during induction dosing of SKYRIZI. For
      the treatment of Crohn's disease and ulcerative colitis, evaluate liver
      enzymes and bilirubin at baseline and during induction (12 weeks); monitor
      thereafter according to routine patient management. Consider an alternate
      treatment for patients with evidence of liver cirrhosis. Interrupt
      treatment if drug-induced liver injury is suspected, until this diagnosis
      is excluded. Instruct your patient to seek immediate medical attention if
      they experience symptoms suggestive of hepatic dysfunction.
    </p>

    <h3 class="text-primary">
      Administration of Vaccines
    </h3>

    <p>
      Avoid use of live vaccines in patients treated with SKYRIZI. Medications
      that interact with the immune system may increase the risk of infection
      following administration of live vaccines. Prior to initiating SKYRIZI,
      complete all age-appropriate vaccinations according to current
      immunization guidelines.
    </p>

    <h3 class="text-primary">
      Adverse Reactions
    </h3>

    <p>
